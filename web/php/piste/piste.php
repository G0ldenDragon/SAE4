            <h2>Présentation du Karting</h2>
            <p>
                Le karting, considéré souvent comme le tremplin pour les sports automobiles de haut niveau, est accessible à tous les âges. Les karts, petits véhicules à quatre roues, sont connus pour leur agilité et leur capacité à atteindre des vitesses impressionnantes sur des circuits relativement courts. Cette activité peut être pratiquée aussi bien pour le loisir que la compétition.
            </p>
            <h2>Règles en Piste</h2>
            <p>Voici quelques-unes des règles importantes à suivre sur la piste :</p>
            <ul>
                <li>Sécurité Avant Tout : Port d'un équipement de protection complet obligatoire.</li>
                <li>Conduite Responsable : Pas de conduite dangereuse ou agressive.</li>
                <li>Respect des Trajectoires : Restez dans les limites de la piste.</li>
            </ul>
            <h2>Signification des Drapeaux en Karting</h2>
            <ul>
                <li><strong>Drapeau Vert :</strong> Début de la course ou reprise après une interruption.</li>
                <li><strong>Drapeau Rouge :</strong> Arrêt immédiat de la course pour cause de danger grave.</li>
                <li><strong>Drapeau Jaune :</strong> Danger sur la piste, ralentissement obligatoire et interdiction de dépasser.</li>
                <li><strong>Drapeau Bleu :</strong> Signal pour laisser passer un concurrent qui vous double.</li>
                <li><strong>Drapeau Noir :</strong> Disqualification d'un concurrent, qui doit quitter la piste.</li>
                <li><strong>Drapeau à Damier :</strong> Fin de la course.</li>
            </ul>
